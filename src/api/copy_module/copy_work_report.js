import CONFIG from '@/assets/js/config';
import http from '@/http';

//小组汇总
export function getCopyGroupSummaryInfo(params = {}) {
    const data = {
        ...CONFIG.ajaxData,
        ...params,
    };
    // 查询参数带isExcel为导出
    if (data.is_excel) {
        let str = '?';
        for (let key in data) {
            str = `${str}&${key}=${data[key] || ''}`;
        }
        window.open('/copywriter/Basic_report/getDataByGroups' + str, '_self');
        return new Promise((resolve => {
            resolve({
                data: {
                    status: 2
                }
            });
        }));
    } else {
        return http.getAjax('/copywriter/Basic_report/getDataByGroups', data);
    }
}

